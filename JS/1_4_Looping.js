// switch syntax
let z;
switch(z){
    
    case " 1":{

        break;
    }
    case " 2":{

        break;
    }
    case " 3":{

        break;
    }

    default:
        {
            // it will executed when no condition gets matched 
        }
}

// we can group two or more cases also 

switch(z){
    
    case " 1":{

        break;
    }
    // grouping case 2 and case 3 
    case " 2": //(*)
    case " 3":{

        break;
    }

    default:
        {
            // it will executed when no condition gets matched 
        }
}


// in switch case type matters it compares according to === and not == 


